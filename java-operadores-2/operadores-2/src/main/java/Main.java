public class Main {

    public static void main(String[] args) {


        int numero = 5;

        numero = - numero;

        System.out.println(numero);

        numero = numero * -1;

        // Incrimentação

        numero = numero + 2;
        numero++; // numero + 1;
        numero--; // numero - 1;

        boolean variavel = true;

        System.out.println(variavel);

        variavel = !variavel;

        System.out.println(variavel);









    }

}
